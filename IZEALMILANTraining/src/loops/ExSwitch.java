package loops;

public class ExSwitch {

	public static void main(String[] args) {
		int marks =75;
		switch(marks)
		{
		case 30: 
			System.out.println("Fail");
			break;
		case 35:
			System.out.println("pass");
			break;
		case 60:
			System.out.println("average");
			break;
		case 75:
			System.out.println("distinction");
			break;
		case 90: 
			System.out.println("good");
			break;
			
		}
		
		
	}

}
